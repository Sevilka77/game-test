function initializeDeck() {
  const deck = []
  Cards.forEach((c) => {
    for (let i = 0; i < c.count[11]; i++) {
      deck.push({
        id: 0,
        title: c.title,
        imagePath: c.imagePath,
        imageBackside: c.imageBackside,
        alt: c.alt,
        on: c.on,
        action: c.action,
        type: c.type,
        description: c.description,
      })
    }
  })
  console.log(deck)
  return deck.map((c, idx) => ({ ...c, id: idx }))
}
function shuffleDeck(deck) {
  for (var i = deck.length - 1; i > 0; i--) {
    var j = Math.floor(Math.random() * (i + 1))
    var temp = deck[i]
    deck[i] = deck[j]
    deck[j] = temp
  }
  return deck
}
export { initializeDeck, shuffleDeck }
